#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*str;
	unsigned char	*str2;

	str = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	while (n--)
		if (*(str++) != *(str2++))
			return ((int)(*(str - 1) - *(str2 - 1)));
	return (0);
}
