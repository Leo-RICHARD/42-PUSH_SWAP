#include <math.h>

float	*vrotate(float **v, float angle)
{
	(*v)[0] = (*v)[0] * cos(angle) + (*v)[0] * sin(angle);
	(*v)[1] = (*v)[1] * -sin(angle) + (*v)[1] * cos(angle);
	return (*v);
}
